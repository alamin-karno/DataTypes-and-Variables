
package datatypes.and.variables;


public class DataTypesAndVariables {

    public static void main(String[] args) {
       
        int num = 10;
        float myFloatNum = 5.0f;
        char myChar = 'A';
        boolean myBol = true;
        String myString = "Alamin Karno";
        
        System.out.println(num);
        System.out.println(myBol);
        System.out.println(myFloatNum);
        System.out.println(myChar);
        System.out.println(myString);
        
        
        int num1 = 5;
        int num2 = 10;
        
        System.out.println(num1);
        System.out.println(num2);
        
        int number1 = 5,number2 = 10;
        
        System.out.println("Multiplication of Two Number: "+ number1*number2);
        System.out.println("Sumation of Two Number: "+ (number1 + number2));
        
        
        
    }
    
}
